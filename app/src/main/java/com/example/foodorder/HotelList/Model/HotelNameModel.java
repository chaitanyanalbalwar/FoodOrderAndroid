package com.example.foodorder.HotelList.Model;

public class HotelNameModel {

    String Name,Image;


    public HotelNameModel(String name, String image) {
        Name = name;
        Image = image;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getImage() {
        return Image;
    }

    public void setImage(String image) {
        Image = image;
    }
}
